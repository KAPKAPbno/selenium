async def get_ai_response(user_id: int, text: str):
    if user_id not in user_history:
        user_history[user_id] = [{"role": "system", "content": AI_INSTRUCTIONS}]

    user_history[user_id].append({"role": "user", "content": text})

    if len(user_history[user_id]) > 11:
        user_history[user_id] = [user_history[user_id][0]] + user_history[user_id][-10:]

    headers = {
        "Authorization": f"Bearer {NVIDIA_API_KEY}",
        "Accept": "application/json"
    }

    payload = {
        "model": MODEL_NAME,
        "messages": user_history[user_id],
        "temperature": 0.35,
        "top_p": 0.8,
        "top_k": 45,
        "max_tokens": 10000,
        "repetition_penalty": 1.1,
        "presence_penalty": 0.2,
        "chat_template_kwargs": {"enable_thinking": True}
    }

    try:
        response = await http_client.post(INVOKE_URL, headers=headers, json=payload)
        response.raise_for_status()
        result = response.json()
        answer = result['choices'][0]['message']['content']

        user_history[user_id].append({"role": "assistant", "content": answer})
        return answer
    except Exception as e:
        logging.error(f"AI Error: {e}")
        return "Ошибка при обращении к нейросети."

async def animate_spinner(message: types.Message, current_phrase: str):
    frame_index = 0

    while True:
        try:
            await asyncio.sleep(0.5)
            frame_index = (frame_index + 1) % len(SPINNER_FRAMES)

            if frame_index % 2 == 0:
                new_phrase = random.choice(LOADING_PHRASES)
                while new_phrase == current_phrase and len(LOADING_PHRASES) > 1:
                    new_phrase = random.choice(LOADING_PHRASES)
                current_phrase = new_phrase

            current_spinner = SPINNER_FRAMES[frame_index]

            await message.edit_text(
                f"{current_spinner} <i>{current_phrase}</i>",
                parse_mode="HTML"
            )
        except asyncio.CancelledError:
            break
        except TelegramBadRequest:
            pass
        except Exception as e:
            logging.error(f"Ошибка анимации: {e}")
            break

