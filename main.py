@dp.message(Command("ai"))
async def handle_ai_command(message: types.Message, command: CommandObject):
    if not command.args:
        return await message.answer("Напиши вопрос после команды, например: `/ai привет, как дела?`",
                                    parse_mode="Markdown")

    await bot.send_chat_action(message.chat.id, "typing")

    start_phrase = random.choice(LOADING_PHRASES)

    loading_msg = await message.reply(
        f"{SPINNER_FRAMES[0]} <i>{start_phrase}</i>",
        parse_mode="HTML"
    )

    spinner_task = asyncio.create_task(animate_spinner(loading_msg, start_phrase))

    try:
        answer = await get_ai_response(message.from_user.id, command.args)
    finally:
        spinner_task.cancel()

    try:
        await loading_msg.delete()
    except Exception:
        pass

    await message.reply(answer)

